import React, { createContext, useContext, useState, useEffect } from 'react';

const LanguageContext = createContext();

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState('en');

  // Load saved language from localStorage
  useEffect(() => {
    const savedLanguage = localStorage.getItem('bridgeLanguage');
    if (savedLanguage && ['en', 'hi', 'or'].includes(savedLanguage)) {
      setCurrentLanguage(savedLanguage);
    }
  }, []);

  // Save language to localStorage when changed
  const changeLanguage = (language) => {
    console.log('Language changed to:', language);
    setCurrentLanguage(language);
    localStorage.setItem('bridgeLanguage', language);
  };

  const value = {
    currentLanguage,
    changeLanguage,
    t: (key) => {
      const translation = translations[currentLanguage]?.[key] || translations.en?.[key] || key;
      // console.log(`Translation for "${key}" in "${currentLanguage}":`, translation);
      return translation;
    }
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

